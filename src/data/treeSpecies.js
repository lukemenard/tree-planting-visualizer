/**
 * Determine which ecoregion a lat/lng is in based on reverse-geocoded state.
 * Returns the ecoregion code or null.
 */
export function getEcoregionForState(stateCode) {
  for (const [regionCode, region] of Object.entries(ECOREGIONS)) {
    if (region.states.includes(stateCode)) return regionCode;
  }
  return null;
}

/**
 * Classify a species for a given ecoregion.
 * Returns 'native' | 'invasive' | 'non-native' | 'unknown'
 */
export function getSpeciesStatus(species, ecoregionCode) {
  if (!ecoregionCode) return 'unknown';
  if (species.invasiveRegions.includes(ecoregionCode)) return 'invasive';
  if (species.nativeRegions.includes(ecoregionCode)) return 'native';
  if (species.nativeRegions.length === 0 && species.invasiveRegions.length === 0) return 'non-native';
  return 'non-native';
}
